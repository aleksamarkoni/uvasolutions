tc = int(sys.stdin.readline().strip())
for tc in range(tc):
    if tc > 0:
        print()
    print("Message #{}".format(tc+1))
    line = sys.stdin.readline().strip()
    if not line:
        print()
        continue
    #python is awesome, problem solved in one line :) :) :) 
    print(''.join([' ' if morse == '' else table[morse] for morse in line.split(' ')]))
